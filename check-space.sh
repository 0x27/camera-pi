#!/bin/sh
df -H | grep rootfs |grep -vE '^Filesystem|tmpfs|cdrom' | awk '{ print $5 " " $1 }' | while read output;
do
  echo $output
  usep=$(echo $output | awk '{ print $1}' | cut -d'%' -f1  )
  partition=$(echo $output | awk '{ print $2 }' )
  if [ $usep -le 95 ]; then
	echo "Okay to take a new snapshot"
  fi
done

